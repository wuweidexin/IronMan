package bean;

public class Course {
	
	private int c_id;
	
	private String c_code;
	
	private String c_name;
	
	private double c_keshi;
	
	private String c_tid;
	
	private String c_tname;
	
	private int c_renshu;
	
	private String c_address;
	
	private String c_begin;
	
	private String c_end;

	public int getC_id() {
		return c_id;
	}

	public void setC_id(int c_id) {
		this.c_id = c_id;
	}

	public String getC_code() {
		return c_code;
	}

	public void setC_code(String c_code) {
		this.c_code = c_code;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}

	public double getC_keshi() {
		return c_keshi;
	}

	public void setC_keshi(double c_keshi) {
		this.c_keshi = c_keshi;
	}

	public String getC_tid() {
		return c_tid;
	}

	public void setC_tid(String c_tid) {
		this.c_tid = c_tid;
	}

	public String getC_tname() {
		return c_tname;
	}

	public void setC_tname(String c_tname) {
		this.c_tname = c_tname;
	}

	public int getC_renshu() {
		return c_renshu;
	}

	public void setC_renshu(int c_renshu) {
		this.c_renshu = c_renshu;
	}

	public String getC_address() {
		return c_address;
	}

	public void setC_address(String c_address) {
		this.c_address = c_address;
	}

	public String getC_begin() {
		return c_begin;
	}

	public void setC_begin(String c_begin) {
		this.c_begin = c_begin;
	}

	public String getC_end() {
		return c_end;
	}

	public void setC_end(String c_end) {
		this.c_end = c_end;
	}

}
