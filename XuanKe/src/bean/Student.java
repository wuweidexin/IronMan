package bean;

public class Student {
	
	private int s_id;
	
	private String s_username;
	
	private String s_password;
	
	private String s_name;
	
	private String s_sex;
	
	private String s_telephone;
	
	private String s_banji;
	
	private String s_zhuanye;

	

	

	public int getS_id() {
		return s_id;
	}

	public void setS_id(int s_id) {
		this.s_id = s_id;
	}

	public String getS_username() {
		return s_username;
	}

	public void setS_username(String s_username) {
		this.s_username = s_username;
	}

	public String getS_password() {
		return s_password;
	}

	public void setS_password(String s_password) {
		this.s_password = s_password;
	}

	public String getS_name() {
		return s_name;
	}

	public void setS_name(String s_name) {
		this.s_name = s_name;
	}

	public String getS_sex() {
		return s_sex;
	}

	public void setS_sex(String s_sex) {
		this.s_sex = s_sex;
	}

	public String getS_telephone() {
		return s_telephone;
	}

	public void setS_telephone(String s_telephone) {
		this.s_telephone = s_telephone;
	}

	public String getS_banji() {
		return s_banji;
	}

	public void setS_banji(String s_banji) {
		this.s_banji = s_banji;
	}

	public String getS_zhuanye() {
		return s_zhuanye;
	}

	public void setS_zhuanye(String s_zhuanye) {
		this.s_zhuanye = s_zhuanye;
	}

}
